#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	x;
	char	i;
	long	n;

	n = nb;
	if (n < 0)
	{
		i = '-';
		write(1, &i, 1);
		n = -n;
	}
	if (n / 10 <= 0)
	{
		i = n + '0';
		write(1, &i, 1);
	}
	else
	{
		x = (n % 10) + '0';
		ft_putnbr(n / 10);
		write(1, &x, 1);
	}
}
